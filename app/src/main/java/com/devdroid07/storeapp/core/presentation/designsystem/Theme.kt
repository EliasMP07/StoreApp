@Composable
fun StoreAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {

    val colorScheme = if(darkTheme) DarkColorScheme else lightScheme
    val  view = LocalView.current
    if (!view.isInEditMode){
        SideEffect {
            val windows = (view.context as Activity).window
            windows.statusBarColor = colorScheme.background.copy(alpha = 0.5f).toArgb()
            windows.navigationBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(windows, view).isAppearanceLightStatusBars = true
        }
    }
    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}